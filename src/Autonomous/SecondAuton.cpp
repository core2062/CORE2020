#include <Autonomous/SecondAuton.h>

SecondAuton::SecondAuton() : COREAuton("Two Path Test Auto") {}

void SecondAuton::AddNodes() {
    m_drivePath = new Node(5, new PathFinderAction("foreward.wpilib.json"));
    m_wait = new Node(3);
    m_secondDrivePath = new Node(5, new PathFinderAction("second.wpilib.json"));
    AddFirstNode(m_drivePath);
    m_drivePath->AddNext(m_wait);
    m_wait->AddNext(m_secondDrivePath);
}
